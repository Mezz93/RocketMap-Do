			var udist = (until.hour > 0) ? until.hour + ':' : ''
			udist += lpad(until.min, 2, 0) + 'm' + lpad(until.sec, 2, 0) + 's'
			find = ['<dist>', '<udist>']
			replace = [dist, udist]
			var ntext = repArray(notifyText, find, replace)
			
